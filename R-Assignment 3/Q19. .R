string1 <- "apple"
string2 <- "Banana"

# Compare the strings using relational operators
is_less <- string1 < string2   # Checks if string1 comes before string2
is_greater <- string1 > string2  # Checks if string1 comes after string2
is_equal <- string1 == string2  # Checks if both strings are identical

print(is_less)      # TRUE if "apple" comes before "Banana"
print(is_greater)   # TRUE if "apple" comes after "Banana"
print(is_equal)     # TRUE if both strings are exactly the same